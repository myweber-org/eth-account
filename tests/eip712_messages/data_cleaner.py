def normalize_column(series, method='zscore'):
    if method == 'zscore':
        return (series - series.mean()) / series.std()
    elif method == 'minmax':
        return (series - series.min()) / (series.max() - series.min())
    else:
        raise ValueError("Method must be 'zscore' or 'minmax'")

