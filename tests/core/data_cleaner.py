def validate_data(data, check_missing=True, check_infinite=True):
    """
    Validate data for common issues.
    """
    validation_report = {}
    
    if check_missing:
        missing_values = data.isnull().sum()
        validation_report['missing_values'] = missing_values[missing_values > 0].to_dict()
    
    if check_infinite:
        numeric_cols = data.select_dtypes(include=[np.number]).columns
        infinite_mask = np.isinf(data[numeric_cols]).any()
        infinite_cols = infinite_mask[infinite_mask].index.tolist()
        validation_report['infinite_values'] = infinite_cols
    
    return validation_report