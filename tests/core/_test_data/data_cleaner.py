        filtered_df = remove_outliers_iqr(cleaned_df, column)
        stats = calculate_basic_stats(filtered_df, column)
        
        print(f"Statistics for {column} after outlier removal:")
        for key, value in stats.items():
            print(f"  {key}: {value:.2f}")
        print("\n")

