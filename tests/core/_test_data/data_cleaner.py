def clean_dataframe(df, strategy='mean', threshold=3):
    """
    Clean a pandas DataFrame by handling missing values and removing outliers.

    Parameters:
    df (pd.DataFrame): Input DataFrame to clean.
    strategy (str): Strategy for filling missing values. Options: 'mean', 'median', 'mode', 'drop'.
    threshold (float): Z-score threshold for outlier detection.

    Returns:
    pd.DataFrame: Cleaned DataFrame.
    """
    cleaned_df = df.copy()

    # Handle missing values
    if strategy == 'drop':
        cleaned_df = cleaned_df.dropna()
    elif strategy == 'mean':
        cleaned_df = cleaned_df.fillna(cleaned_df.mean(numeric_only=True))
    elif strategy == 'median':
        cleaned_df = cleaned_df.fillna(cleaned_df.median(numeric_only=True))
    elif strategy == 'mode':
        cleaned_df = cleaned_df.fillna(cleaned_df.mode().iloc[0])
    else:
        raise ValueError("Invalid strategy. Choose from 'mean', 'median', 'mode', 'drop'.")

    # Remove outliers using Z-score method for numeric columns
    numeric_cols = cleaned_df.select_dtypes(include=[np.number]).columns
    for col in numeric_cols:
        z_scores = np.abs((cleaned_df[col] - cleaned_df[col].mean()) / cleaned_df[col].std())
        cleaned_df = cleaned_df[z_scores < threshold]

    # Reset index after cleaning
    cleaned_df = cleaned_df.reset_index(drop=True)

    return cleaned_df

def validate_dataframe(df):
    """
    Validate DataFrame for basic integrity checks.

    Parameters:
    df (pd.DataFrame): DataFrame to validate.

    Returns:
    dict: Dictionary containing validation results.
    """
    validation_results = {
        'total_rows': len(df),
        'total_columns': len(df.columns),
        'missing_values': df.isnull().sum().sum(),
        'duplicate_rows': df.duplicated().sum(),
        'numeric_columns': list(df.select_dtypes(include=[np.number]).columns),
        'categorical_columns': list(df.select_dtypes(include=['object']).columns)
    }
    return validation_results

