def _normalize_string(text):
    """Normalize a string by removing extra whitespace and converting to lowercase."""
    if pd.isna(text):
        return text
    
    normalized = str(text).strip()
    normalized = re.sub(r'\s+', ' ', normalized)
    normalized = normalized.lower()
    
    return normalized

