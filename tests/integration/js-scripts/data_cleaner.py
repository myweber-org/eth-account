def normalize_columns(df, columns=None):
    """
    Normalize specified columns to have values between 0 and 1.
    
    Args:
        df (pd.DataFrame): Input DataFrame
        columns (list): List of column names to normalize. If None, normalize all numeric columns.
    
    Returns:
        pd.DataFrame: DataFrame with normalized columns
    """
    normalized_df = df.copy()
    
    if columns is None:
        columns = normalized_df.select_dtypes(include=[np.number]).columns
    
    for column in columns:
        if column in normalized_df.columns and normalized_df[column].dtype in [np.float64, np.int64]:
            col_min = normalized_df[column].min()
            col_max = normalized_df[column].max()
            
            if col_max != col_min:
                normalized_df[column] = (normalized_df[column] - col_min) / (col_max - col_min)
            else:
                normalized_df[column] = 0.5
    
    return normalized_df
