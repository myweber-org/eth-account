def remove_outliers(df, columns=None, method='iqr', threshold=1.5):
    """
    Remove outliers from specified columns using IQR or Z-score method.
    
    Parameters:
    df (pd.DataFrame): Input DataFrame.
    columns (list): Columns to process. If None, process all numeric columns.
    method (str): 'iqr' for interquartile range or 'zscore' for standard deviations.
    threshold (float): Threshold multiplier for IQR or Z-score cutoff.
    
    Returns:
    pd.DataFrame: DataFrame with outliers removed.
    """
    if columns is None:
        columns = df.select_dtypes(include=[np.number]).columns
    
    cleaned_df = df.copy()
    
    for col in columns:
        if col not in cleaned_df.columns or not pd.api.types.is_numeric_dtype(cleaned_df[col]):
            continue
            
        if method == 'iqr':
            Q1 = cleaned_df[col].quantile(0.25)
            Q3 = cleaned_df[col].quantile(0.75)
            IQR = Q3 - Q1
            lower_bound = Q1 - threshold * IQR
            upper_bound = Q3 + threshold * IQR
            mask = (cleaned_df[col] >= lower_bound) & (cleaned_df[col] <= upper_bound)
        elif method == 'zscore':
            z_scores = np.abs((cleaned_df[col] - cleaned_df[col].mean()) / cleaned_df[col].std())
            mask = z_scores <= threshold
        else:
            continue
            
        cleaned_df = cleaned_df[mask]
    
    return cleaned_df.reset_index(drop=True)
