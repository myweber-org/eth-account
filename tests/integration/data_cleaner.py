def detect_missing_patterns(data):
    """
    Detect patterns in missing data.
    
    Args:
        data: pandas DataFrame
    
    Returns:
        Dictionary with missing data analysis
    """
    missing_counts = data.isnull().sum()
    missing_percentage = (missing_counts / len(data)) * 100
    
    missing_info = {
        'total_rows': len(data),
        'total_columns': len(data.columns),
        'missing_counts': missing_counts[missing_counts > 0].to_dict(),
        'missing_percentage': missing_percentage[missing_percentage > 0].to_dict(),
        'complete_cases': data.dropna().shape[0],
        'complete_case_percentage': (data.dropna().shape[0] / len(data)) * 100
    }
    
    return missing_info